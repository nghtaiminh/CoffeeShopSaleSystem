    private void comboBoxPaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxPaymentActionPerformed
        if (comboBoxPayment.getSelectedItem().equals("Cash")) {
            tfInputCardID.setEditable(false);
        }
        else if (comboBoxPayment.getSelectedItem().equals("Credit Card")) {
            tfInputCardID.setEditable(true);
            txtCardID.setText("Card ID");
        }
        else {
            tfInputCardID.setEditable(true);
            txtCardID.setText("Wallet ID");
        }
    }//GEN-LAST:event_comboBoxPaymentActionPerformed

