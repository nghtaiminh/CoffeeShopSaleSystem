    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        int quantity = (int) jSpinnerQuantity.getValue();
        String name = tfProductName.getText().trim();
        int price;
        price = Integer.parseInt(tfPrice.getText().trim());
         
       if (name.equals("") || quantity == 0){
           JOptionPane.showMessageDialog(this, "Please select the item!");
       }
       else {
           totalCost += price*quantity;
           String exitPrice = Integer.toString(price*quantity);
           String perPrice = Integer.toString(price);
           String units = Integer.toString(quantity);
           String data[] = {name, perPrice,units, "0", exitPrice};
           
           DefaultTableModel tblModel = (DefaultTableModel) orderTable.getModel();
           // Add string to array
           tblModel.addRow(data);
           // Blank and update total cost
           tfProductName.setText("");
           tfPrice.setText("");
           jSpinnerQuantity.setValue(0);
           txtTotalCost.setText(Integer.toString(totalCost));
       }
    }//GEN-LAST:event_btnAddItemActionPerformed

    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        DefaultTableModel model = (DefaultTableModel)jTable3.getModel();
        int selectedRowIndex = jTable3.getSelectedRow();
        
        tfProductName.setText(model.getValueAt(selectedRowIndex, 0).toString());
        tfPrice.setText(model.getValueAt(selectedRowIndex, 1).toString());
        jSpinnerQuantity.setValue(1);    
    }//GEN-LAST:event_jTable3MouseClicked

    private void btnRemoveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveItemActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel) orderTable.getModel();
        
        // delete roow
        if (orderTable.getSelectedRowCount() == 1){
            // if single row is selected then delete and update total cost
            int removeCost =Integer.parseInt(tblModel.getValueAt(orderTable.getSelectedRow(), 4).toString());
            totalCost -= removeCost;
            tblModel.removeRow(orderTable.getSelectedRow());
            txtTotalCost.setText(Integer.toString(totalCost));  
        }
        else {
            if (orderTable.getRowCount() == 0){
                // if table empty => display message
                JOptionPane.showMessageDialog(this, "Order is Empty.");
            }
            else {
                // not empty but (no row is selected) OR (multiple row is selected)
                JOptionPane.showMessageDialog(this, "Select a Single Row in Order Form For Delete");
            }
        }
    }//GEN-LAST:event_btnRemoveItemActionPerformed
   
